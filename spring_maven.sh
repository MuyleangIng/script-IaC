#!/bin/bash

# Function to check if a command exists
command_exists () {
    command -v "$1" >/dev/null 2>&1
}

# Check if Maven is installed
if ! command_exists mvn ; then
    echo "Maven is not installed. Please install Maven before running this script."
    exit 1
fi

# Function to create a Maven project
create_service() {
    SERVICE_NAME=$1
    POM_CONTENT=$2

    mkdir $SERVICE_NAME
    cd $SERVICE_NAME || exit

    # Create pom.xml
    echo "$POM_CONTENT" > pom.xml

    # Create main application class
    mkdir -p src/main/java/com/sen/$SERVICE_NAME
    cat <<EOF > src/main/java/com/sen/$SERVICE_NAME/Application.java
package com.sen.$SERVICE_NAME;

import org.springframework.boot.SpringApplication;
import org.springframework.boot.autoconfigure.SpringBootApplication;

@SpringBootApplication
public class Application {
    public static void main(String[] args) {
        SpringApplication.run(Application.class, args);
    }
}
EOF

    cd ..
}

